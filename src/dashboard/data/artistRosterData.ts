export type RosterStatus = 'Active' | 'On Hold' | 'Recouping' | 'Inactive' | 'Priority' | 'New Signing' | 'Pending Sync';
export type RosterTier = 'Growth Roster' | 'Base Roster' | 'Label Roster' | 'Priority';

export interface ArtistFinancials {
  advance: number;
  artistGrant: number;
  artistGrantRecoupable: boolean;
  adSpend: { allTime: number; ytd: number; last30: number };
  marketingSpend: { allTime: number; ytd: number; last30: number };
  liveShows: { allTime: number; ytd: number; last30: number };
  touring: { allTime: number; ytd: number; last30: number };
  contentProduction: { allTime: number; ytd: number; last30: number };
  arSpend: { allTime: number; ytd: number; last30: number };
  operationsPeople: { allTime: number; ytd: number; last30: number };
  otherRecoupable: { allTime: number; ytd: number; last30: number };
  otherNonRecoupable: { allTime: number; ytd: number; last30: number };
  totalInvestment: { allTime: number; ytd: number; last30: number };
  totalRecoupableSpend: { allTime: number; ytd: number; last30: number };
  totalNonRecoupableSpend: { allTime: number; ytd: number; last30: number };
  recoupableBalance: number;
  ytdRevenue: number;
  allTimeRevenue: number;
  last30Revenue: number;
}

export type ReleaseStatus = 'Released' | 'In Production' | 'Scheduled' | 'Blocked' | 'Pre-Save Live' | 'No Active Release';
export type CampaignStage = 'Pre-Release' | 'Launch Week' | 'Active Push' | 'Sustain' | 'Wind Down' | 'Standby';

export interface ArtistRelease {
  id: string;
  title: string;
  type: 'Single' | 'EP' | 'Album' | 'Mixtape';
  status: ReleaseStatus;
  releaseDate: string;
  campaignStage: CampaignStage;
  streams?: number;
  coverNote?: string;
}

export interface ArtistAction {
  id: string;
  label: string;
  priority: 'urgent' | 'high' | 'normal';
  owner: string;
  due?: string;
  category: 'campaign' | 'release' | 'financial' | 'ops' | 'creative';
}

